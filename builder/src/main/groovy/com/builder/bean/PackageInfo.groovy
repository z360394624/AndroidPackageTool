package com.builder.bean

public class PackageInfo {

    String apk
    String md5

    String getApk() {
        return apk
    }

    void setApk(String apk) {
        this.apk = apk
    }

    String getMd5() {
        return md5
    }

    void setMd5(String md5) {
        this.md5 = md5
    }
}